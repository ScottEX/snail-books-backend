def lookup(prefix: str) -> Optional[Dict[str, str]]:
    """Look up bank info by the first 6 digits of a card number.

    Returns {name, code} or None if not found.
    """
    if not prefix or len(prefix) < 6:
        return None
    key = prefix[:6]
    return BANK_BINS.get(key)

